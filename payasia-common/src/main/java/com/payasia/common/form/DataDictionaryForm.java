package com.payasia.common.form;

public class DataDictionaryForm {

	private String dataDictionaryId;
	private String tableSeqId;
	private Long employeeId;
	private Long companyId;
	
	public String getTableSeqId() {
		return tableSeqId;
	}

	public void setTableSeqId(String tableSeqId) {
		this.tableSeqId = tableSeqId;
	}

	public Long getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(Long employeeId) {
		this.employeeId = employeeId;
	}

	public Long getCompanyId() {
		return companyId;
	}

	public void setCompanyId(Long companyId) {
		this.companyId = companyId;
	}

	public String getDataDictionaryId() {
		return dataDictionaryId;
	}

	public void setDataDictionaryId(String dataDictionaryId) {
		this.dataDictionaryId = dataDictionaryId;
	}
	
	
}
