package com.payasia.common.dto;

public class CareerProgressionPHReportDTO {
	
	private String employeeID;
	private String employeeName;
	private String careerProgressionReason;
	private String careerProgressionDate;
	private String department;
	private String jobTitle;
	private String costCenter;
	private String remarks;
	
	public String getEmployeeID() {
		return employeeID;
	}
	public void setEmployeeID(String employeeID) {
		this.employeeID = employeeID;
	}
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public String getCareerProgressionReason() {
		return careerProgressionReason;
	}
	public void setCareerProgressionReason(String careerProgressionReason) {
		this.careerProgressionReason = careerProgressionReason;
	}
	public String getCareerProgressionDate() {
		return careerProgressionDate;
	}
	public void setCareerProgressionDate(String careerProgressionDate) {
		this.careerProgressionDate = careerProgressionDate;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	public String getCostCenter() {
		return costCenter;
	}
	public void setCostCenter(String costCenter) {
		this.costCenter = costCenter;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	

}
