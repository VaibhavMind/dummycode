package com.payasia.common.dto;

public class TimeOffAUReportDTO {
	
	private String employeeID;
	private String lastName;
	private String firstName;
	private String timeOffCode;
	private String quantity;
	private String unitofTime;
	
	public String getEmployeeID() {
		return employeeID;
	}
	public void setEmployeeID(String employeeID) {
		this.employeeID = employeeID;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getTimeOffCode() {
		return timeOffCode;
	}
	public void setTimeOffCode(String timeOffCode) {
		this.timeOffCode = timeOffCode;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getUnitofTime() {
		return unitofTime;
	}
	public void setUnitofTime(String unitofTime) {
		this.unitofTime = unitofTime;
	}
	
	

}
