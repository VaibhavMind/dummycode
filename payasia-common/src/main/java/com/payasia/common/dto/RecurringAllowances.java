package com.payasia.common.dto;

public class RecurringAllowances {
	
	private String eeeRef;
	private String forename;
	private String surname;
	private String payCode;
	private String payDescription;
	private String recurringAmount;
	private String startYear;
	private String startMonth;
	private String endYear;
	private String endMonth;
	private String remarks;
	
	public String getEeeRef() {
		return eeeRef;
	}
	public void setEeeRef(String eeeRef) {
		this.eeeRef = eeeRef;
	}
	public String getForename() {
		return forename;
	}
	public void setForename(String forename) {
		this.forename = forename;
	}
	public String getSurname() {
		return surname;
	}
	public void setSurname(String surname) {
		this.surname = surname;
	}
	public String getPayCode() {
		return payCode;
	}
	public void setPayCode(String payCode) {
		this.payCode = payCode;
	}
	public String getPayDescription() {
		return payDescription;
	}
	public void setPayDescription(String payDescription) {
		this.payDescription = payDescription;
	}
	public String getRecurringAmount() {
		return recurringAmount;
	}
	public void setRecurringAmount(String recurringAmount) {
		this.recurringAmount = recurringAmount;
	}
	public String getStartYear() {
		return startYear;
	}
	public void setStartYear(String startYear) {
		this.startYear = startYear;
	}
	public String getStartMonth() {
		return startMonth;
	}
	public void setStartMonth(String startMonth) {
		this.startMonth = startMonth;
	}
	public String getEndYear() {
		return endYear;
	}
	public void setEndYear(String endYear) {
		this.endYear = endYear;
	}
	public String getEndMonth() {
		return endMonth;
	}
	public void setEndMonth(String endMonth) {
		this.endMonth = endMonth;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	

}
